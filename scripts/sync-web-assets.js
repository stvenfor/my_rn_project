#!/usr/bin/env node
/**
 * Sync WebView local test assets from Flutter my_ai_project (or package source)
 * to Android / Harmony native dirs and regenerate iOS inline bundle module.
 *
 * Usage: node scripts/sync-web-assets.js
 */
const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..');
const flutter = path.join(root, '../my_ai_project');
const sourceDir = path.join(root, 'packages/core/webview/assets/web');

const WEB_FILES = ['test_bridge.html', 'ICSAPPInjection.js'];
/** HTML parity from Flutter; bridge script stays RN-specific in package. */
const FLUTTER_SYNC_FILES = ['test_bridge.html'];

function cp(src, dest) {
  fs.mkdirSync(path.dirname(dest), {recursive: true});
  fs.copyFileSync(src, dest);
}

function escapeTemplateLiteral(value) {
  return value.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
}

function syncFromFlutter() {
  if (!fs.existsSync(flutter)) {
    console.warn(
      '[sync-web-assets] Flutter project not found, using package assets only:',
      flutter,
    );
    return;
  }
  fs.mkdirSync(sourceDir, {recursive: true});
  for (const file of FLUTTER_SYNC_FILES) {
    const flutterFile = path.join(flutter, 'assets/web', file);
    if (fs.existsSync(flutterFile)) {
      cp(flutterFile, path.join(sourceDir, file));
      console.log('[sync-web-assets] synced from Flutter:', file);
    }
  }
  patchTestBridgeHtml();
  const flutterBridge = path.join(flutter, 'assets/web/ICSAPPInjection.js');
  if (fs.existsSync(flutterBridge)) {
    cp(
      flutterBridge,
      path.join(sourceDir, 'ICSAPPInjection.js.sourceflutter'),
    );
    console.log('[sync-web-assets] saved Flutter bridge reference copy');
  }
}

function patchTestBridgeHtml() {
  const htmlPath = path.join(sourceDir, 'test_bridge.html');
  if (!fs.existsSync(htmlPath)) {
    return;
  }
  let html = fs.readFileSync(htmlPath, 'utf8');
  html = html
    .replace(
      '<h1>Flutter ↔ H5 桥接测试</h1>',
      '<h1>App ↔ H5 桥接测试</h1>',
    )
    .replace(
      '<h2>Flutter 注入参数 (__FLUTTER_PARAMS__)</h2>',
      '<h2>注入参数 (__RN_PARAMS__ / __FLUTTER_PARAMS__)</h2>',
    )
    .replace(
      '等待 Flutter 注入...',
      '等待 App 注入...',
    )
    .replace(
      'function renderParams() {\n      var params = window.__FLUTTER_PARAMS__ || {};',
      'function readParams() {\n      return window.__RN_PARAMS__ || window.__FLUTTER_PARAMS__ || {};\n    }\n\n    function renderParams() {\n      var params = readParams();',
    )
    .replace(
      "log('→ Flutter: action='",
      "log('→ App: action='",
    )
    .replace(
      "log('← Flutter: '",
      "log('← App: '",
    )
    .replace(
      `window.addEventListener('flutterReady', function() {
      renderParams();
      log('收到 flutterReady 事件');
    });`,
      `function onBridgeReady() {
      renderParams();
      log('收到 bridge ready 事件');
    }

    window.addEventListener('rnReady', onBridgeReady);
    window.addEventListener('flutterReady', onBridgeReady);`,
    );
  fs.writeFileSync(htmlPath, html, 'utf8');
  console.log('[sync-web-assets] patched test_bridge.html for RN parity');
}

function copyToNative() {
  const androidDir = path.join(root, 'android/app/src/main/assets/web');
  const harmonyDir = path.join(
    root,
    'harmony/entry/src/main/resources/rawfile/web',
  );
  const iosDir = path.join(root, 'ios/MyRnProject/web');

  for (const dir of [androidDir, harmonyDir, iosDir]) {
    fs.mkdirSync(dir, {recursive: true});
  }

  for (const file of WEB_FILES) {
    const src = path.join(sourceDir, file);
    if (!fs.existsSync(src)) {
      console.error('[sync-web-assets] missing source asset:', src);
      process.exit(1);
    }
    cp(src, path.join(androidDir, file));
    cp(src, path.join(harmonyDir, file));
    cp(src, path.join(iosDir, file));
  }
}

function generateWebAssetContent() {
  const html = fs.readFileSync(
    path.join(sourceDir, 'test_bridge.html'),
    'utf8',
  );
  const bridgeJs = fs.readFileSync(
    path.join(sourceDir, 'ICSAPPInjection.js'),
    'utf8',
  );

  const out = `/* eslint-disable */
/** Auto-generated by scripts/sync-web-assets.js — do not edit manually. */

export const TEST_BRIDGE_HTML = \`${escapeTemplateLiteral(html)}\`;

export const ICS_APP_INJECTION_JS = \`${escapeTemplateLiteral(bridgeJs)}\`;

const WEB_ASSET_HTML: Record<string, string> = {
  'assets/web/test_bridge.html': TEST_BRIDGE_HTML,
};

const WEB_ASSET_SCRIPT: Record<string, string> = {
  'assets/web/ICSAPPInjection.js': ICS_APP_INJECTION_JS,
};

export function getWebAssetHtml(assetPath: string): string {
  const html = WEB_ASSET_HTML[assetPath];
  if (!html) {
    throw new Error(\`Unknown web asset html: \${assetPath}\`);
  }
  return html;
}

export function getWebAssetScript(assetPath: string): string {
  const script = WEB_ASSET_SCRIPT[assetPath];
  if (!script) {
    throw new Error(\`Unknown web asset script: \${assetPath}\`);
  }
  return script;
}
`;

  const dest = path.join(
    root,
    'packages/core/webview/src/webAssetContent.ts',
  );
  fs.writeFileSync(dest, out, 'utf8');
  console.log('[sync-web-assets] generated', path.relative(root, dest));
}

syncFromFlutter();
copyToNative();
generateWebAssetContent();
console.log('[sync-web-assets] done');
